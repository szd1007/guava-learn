public class HashCollison {
    public static void main(String[] args) {
        System.out.println("AaAaAaAaAaAaBBBB".hashCode());
        System.out.println("AaAaBBBBAaAaBBBB".hashCode());
        System.out.println("AaAaAaBBAaAaAaAa".hashCode());
        System.out.println("AaAaAaAaAaAaAaAa".hashCode());
    }
}
